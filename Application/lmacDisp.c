static I2C_HandleTypeDef* lmadDisp_EnableDisp(EDispNum_t disp)
{
	I2C_HandleTypeDef* hi2c = NULL;
	switch (disp) {
		case EDispNum_1: LCD_UP_ENABLE;	hi2c = &hi2c1;	break;	// 左上
		case EDispNum_2: LCD_UP_ENABLE;	hi2c = &hi2c3;	break;	// 右上
		case EDispNum_3: LCD_DN_ENABLE;	hi2c = &hi2c1;	break;	// 左下
		case EDispNum_4: LCD_DN_ENABLE;	hi2c = &hi2c3;	break;	// 右下
		default:	break;
	}
	return hi2c;
}


static uint8_t* lmacDisp_GetDispBuf(EDispNum_t disp)
{
	uint8_t* buf = NULL;
	switch (disp) {
		case EDispNum_1: buf = LcdBuf1;		break;	// 左上
		case EDispNum_2: buf = LcdBuf2;		break;	// 右上
		case EDispNum_3: buf = LcdBuf3;		break;	// 左下
		case EDispNum_4: buf = LcdBuf4;		break;	// 右下
		default:	break;
	}
	return buf;
}


static void lmadDisp_DisableDisp(EDispNum_t disp)
{
	switch (disp) {
		case EDispNum_1: LCD_UP_DISABLE;	break;	// 左上
		case EDispNum_2: LCD_UP_DISABLE;	break;	// 右上
		case EDispNum_3: LCD_DN_DISABLE;	break;	// 左下
		case EDispNum_4: LCD_DN_DISABLE;	break;	// 右下
		default:	break;
	}
}


/*!*************************************************
* @fn	文字をディスプレイバッファに書き込む
* @param	ディスプレイ番号
* @param	x座標
* @param	y座標(ページ単位)
* @param	文字(font_num)
* @param	文字幅
* @return	None
*//*************************************************/
void lmacDisp_SetChar(EDispNum_t disp, uint16_t x, uint16_t y, uint8_t char_id, uint16_t font_wid)
{
	uint8_t* buf = lmacDisp_GetDispBuf(disp);
	uint8_t* font = font_num[char_id];
	for (int ty=0; ty<8; ty++) {
		for (int tx=0; tx<font_wid; tx++) {
			buf[1+ x + tx + (y+ty) * 128] = font[tx + ty * font_wid];
		}
	}
}


/*!*************************************************
* @fn	グリフをディスプレイバッファに書き込む
* @param	ディスプレイ番号
* @param	x座標
* @param	y座標(ページ単位)
* @param	グリフのポインタ
* @param	グリフの幅
* @return	None
*//*************************************************/
void lmacDisp_SetGlyph(EDispNum_t disp, uint16_t x, uint16_t y, uint8_t* font, uint16_t font_wid, uint16_t font_height)
{
	uint8_t* buf = lmacDisp_GetDispBuf(disp);
	for (int ty=0; ty<font_height; ty++) {
		for (int tx=0; tx<font_wid; tx++) {
			buf[1+ x + tx + (y+ty) * 128] = font[tx + ty * font_wid];
		}
	}
}


/*!*************************************************
* @fn	初期化
* @param	None
* @return	None
*//*************************************************/
void lmacDisp_Initialize(void)
{
	// Hardware Init
	LCD_UP_ENABLE;
	LCD_DN_ENABLE;

	uint8_t	cmd[] = {
		0x00,		// Control byte Co=0, D/C#=0 (Co=0 とすると以後連続して1バイトづつ送れる)
		0x8D,		// Set charge pump
		0x14,		// Enable charge pump
		0xAF		// Display ON
	};
	HAL_I2C_Master_Transmit(&hi2c1, SSD1780_I2C_ADDR , cmd, sizeof(cmd), 1000);
	HAL_I2C_Master_Transmit(&hi2c3, SSD1780_I2C_ADDR , cmd, sizeof(cmd), 1000);

	LCD_UP_DISABLE;
	LCD_DN_DISABLE;

	// Initial Disp
	lmacDisp_SetGlyph(EDispNum_1, 0, 0, font_Temp, 16, 8);		lmacDisp_SetGlyph(EDispNum_1, 104, 4, font_TempUnit, 14, 4);
	lmacDisp_SetGlyph(EDispNum_3, 0, 0, font_Humid, 15, 8);		lmacDisp_SetGlyph(EDispNum_3, 104, 4, font_HumidUnit, 13, 4);
	lmacDisp_SetGlyph(EDispNum_2, 0, 0, font_CO2, 20, 4);		lmacDisp_SetGlyph(EDispNum_2, 104, 4, font_CO2Unit, 24, 4);
	lmacDisp_SetGlyph(EDispNum_4, 0, 0, font_Press, 16, 8);		lmacDisp_SetGlyph(EDispNum_4, 104, 4, font_PressUnit, 23, 4);
}


static void lmacDisp_Update(EDispNum_t disp_num)
{
	I2C_HandleTypeDef* hi2c = lmadDisp_EnableDisp(disp_num);
	uint8_t* buf = lmacDisp_GetDispBuf(disp_num);

	for (int page = 0; page < 8; page++) {
		uint8_t	cmd[] = {
			0x00,		// Control byte Co=0, D/C#=0 (Co=0 とすると以後連続して1バイトづつ送れる)
			0x20,		// Set memory addressing mode
			0x00,		// Horizontal addressing mode
			0x21,		// Set column address
			0x00,		// Column start address 0
			0x7F,		// Column end address 127d
			0x22,		// Set page address
			0x00,		// Page start address 0
			0x07,		// Page end address 7d
		};
		HAL_I2C_Master_Transmit(hi2c, SSD1780_I2C_ADDR , cmd, sizeof(cmd), 1000);

		//uint8_t ctrl = 0x40;
		HAL_I2C_Master_Transmit(hi2c, SSD1780_I2C_ADDR , buf, sizeof(LcdBuf1), 1000);
	}

	lmadDisp_DisableDisp(disp_num);
}


/*!*************************************************
* @fn	表示器のメインループ
* @breaf	デスプレイバッファのデータをディスプレイに転送する
* @param	None
* @return	None
*//*************************************************/
void lmacDisp_MainLoop(void)
{
	for (EDispNum_t disp=0; disp<4; disp++) {
		lmacDisp_Update(disp);
	}
}
